    <h2 style="color: #002a69; text-align: center; font-size: 2em; margin-bottom: 20px;">
      Qué es la ETA
    </h2>

    <p>
      La <strong>ETA (Electronic Travel Authorization)</strong> es un sistema de preselección en línea que permite a las autoridades migratorias evaluar a los viajeros antes de su llegada. Su objetivo es reforzar la seguridad fronteriza y agilizar el proceso de entrada para turistas, personas de negocios o aquellos en tránsito.
    </p>

    <h3 style="color: #002a69; margin-top: 20px;">Puntos clave:</h3>
    <ul>
      <li><strong>Es obligatoria:</strong> Debes obtenerla antes de reservar tu vuelo y presentarte en el aeropuerto.</li>
      <li><strong>Se vincula electrónicamente a tu pasaporte:</strong> No necesitas un documento físico especial; el personal de la aerolínea escaneará tu pasaporte para confirmar tu autorización.</li>
      <li><strong>Permite múltiples entradas:</strong> Generalmente es válida por hasta cinco años o hasta que tu pasaporte expire (lo que ocurra primero), permitiendo estancias cortas, usualmente de hasta seis meses por visita.</li>
      <li><strong>No garantiza la entrada:</strong> Un oficial de servicios fronterizos tomará la decisión final sobre tu admisión al llegar al país.</li>
    </ul>

